let sampleValue1 = 123;
console.log(typeof sampleValue1);

let sampleValue2 = true;
console.log(typeof sampleValue2);

let sampleValue3 = "hello world";
console.log(typeof sampleValue3);

// typeof의 자료형은 string이다.
let value = typeof 123;
console.log(value);
console.log(typeof value);

let sampleValue4 = null;
console.log(typeof sampleValue4);

// null은 object형이다
console.log(typeof null);

let sampleValue6;
console.log(sampleValue6); // undefined인 변수를 출력하면 ?
console.log(typeof sampleValue6);

// undefined
let sampleValue7 = undefined;
console.log(sampleValue7);
console.log(typeof sampleValue7);

// undefined는 undefined 형이다
console.log(typeof undefined);

// undefined가 null의 일종이라면 typeof null을 했을 때와 typeof undefined를 했을 때 왜 다른지?
